#include "shell.h"
/**
 * str_to_int - converts string to integer
 * @c: the characters to convert
 * Return: the convert
 */
int str_to_int(const char *c)
{
	int convert = atoi(c);
	return convert;
}

/**
 * int_to_str - converts integer to string
 * @num: the integer to be converted
 * @string: the convert
 */
void int_to_str(int num, char* string)
{
	printf(string, "%d", number);
}
